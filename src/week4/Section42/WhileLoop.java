package week4.Section42;

public class WhileLoop {
    public static void main(String[] args) {

        int counter = 1;
        while(counter <= 5) {
            System.out.println("Superstar");
            counter++;
        }

        int counter2 = 1;
        while(counter2 <= 20) {
            System.out.println("I love Java");
            counter2++;
        }
    }
}
