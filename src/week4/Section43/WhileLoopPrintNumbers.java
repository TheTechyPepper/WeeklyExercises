package week4.Section43;

public class WhileLoopPrintNumbers {
    public static void main(String [] args) {

        int counter = 1;

        while (counter <= 5) {
            System.out.println(counter);
            counter++;
        }

        int counter2 = 0;
        while (counter2 <= 9) {
            System.out.println(counter2);
            counter2++;
        }
    }
}
