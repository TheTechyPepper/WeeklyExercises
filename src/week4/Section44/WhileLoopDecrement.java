package week4.Section44;

public class WhileLoopDecrement {
    public static void main(String[] args) {

        int counter = 5;

        while (counter >= 1) {
            System.out.println(counter);
            counter--;
        }

        int counter2 = 17;
        while (counter2 >= 9) {
            System.out.println(counter2);
            counter2--;
        }
    }
}
